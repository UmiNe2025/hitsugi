// 経過年で郷人も老いる(0-2年/3-5年/6-9年/10年〜)
function band(years: number): 0 | 1 | 2 | 3 {
  if (years < 3) return 0
  if (years < 6) return 1
  if (years < 10) return 2
  return 3
}

// プレースホルダ置換
function fill(template: string, ctx: { head: string; gen: number; fallen: number }): string {
  return template
    .replaceAll('{head}', ctx.head)
    .replaceAll('{gen}', String(ctx.gen))
    .replaceAll('{fallen}', String(ctx.fallen))
}

// 未登録idの保険(データ追加漏れでも郷は沈黙しない)
const GENERIC_LINES: [string, string] = [
  '{head}さまか。……ああ、燈守の家の。いつも郷を守ってくれて、ありがとうな。',
  '大燈籠の火が今夜も灯っとる。それだけで、わしらは眠れるんだ。',
]

export function villagerLine(vid: string, data: GameData): { name: string; text: string } {
  const years = Math.floor(data.seasonIndex / 12)
  const b = band(years)
  const head = data.family.find((c) => c.alive && c.isHead)
  const ctx = {
    head: head?.name ?? 'あんた',
    gen: head?.gen ?? 1,
    fallen: data.family.filter((c) => !c.alive).length,
  }
  const def = VILLAGERS.find((x) => x.id === vid)
  if (!def) {
    return { name: '郷の者', text: fill(GENERIC_LINES[data.seasonIndex % 2], ctx) }
  }
  return { name: def.names[b], text: fill(def.lines[b][data.seasonIndex % 2], ctx) }
}
